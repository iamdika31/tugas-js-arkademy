const a = 10;
const b = 10;
const c = 13;

// lebih dari atau >
console.log(a > b);

// kurang dari atau <
console.log(a < c);

// sama dengan double atau = =
// membandingkan nilai tanpa memperdulikan tipe data nya.
console.log(a == '10');

// sama dengan triple atau = = =
// membandingkan nilai dan tipe data
console.log(a === '10');

// tidak sama dengan
// nilai tidak sama dengan tanpa memperdulikan tipe data 
console.log(a !='10');

// tidak sama dengan
// nilai tidak sama dengan dengan memperhatikan tipe data
console.log(a !== 11);

// lebih besar dari sama dengan
// membandingkan antar nilai yang lebih besar sama dengan 
console.log(a >= 10);

// lebih kecil dari sama dengan
// membandingkan antar nilai yang lebih kecil sama dengan 
console.log(a <= 9);



